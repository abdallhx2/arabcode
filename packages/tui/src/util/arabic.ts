/**
 * تطبيع النصّ العربي لأغراض البحث والمطابقة.
 *
 * يجعل البحث متسامحاً مع اختلافات الكتابة الشائعة التي لا يفرّق المستخدم بينها ذهنياً:
 * الهمزات وأشكال الألف، التاء المربوطة، الألف المقصورة، الحركات، والتطويل.
 * فيطابق «انشئ» ما كُتب «أنشئ»، و«الطرفيه» ما كُتب «الطرفية».
 *
 * النصوص غير العربية تمرّ دون تغيير (عدا خفض الحالة اختيارياً في موضع الاستدعاء).
 */

// حركات التشكيل + العلامات فوق/تحت الحرف + التطويل (ـ)
const DIACRITICS = /[ؐ-ًؚ-ٰٟۖ-ۜ۟-۪ۨ-ۭـ]/g

export function normalizeArabic(input: string): string {
  return input
    .replace(DIACRITICS, "")
    .replace(/[أإآٱء]/g, "ا") // أ إ آ ٱ ء → ا
    .replace(/ى/g, "ي") // ى → ي
    .replace(/ؤ/g, "و") // ؤ → و
    .replace(/ئ/g, "ي") // ئ → ي
    .replace(/ة/g, "ه") // ة → ه
}

/** تطبيع + خفض الحالة، للاستخدام كمفتاح بحث موحّد للعربية واللاتينية معاً. */
export function searchKey(input: string): string {
  return normalizeArabic(input.toLowerCase())
}
